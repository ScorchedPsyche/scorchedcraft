package com.github.scorchedpsyche.scorchedcraft.fabric.core.mixin;

import net.minecraft.client.render.Camera;
import net.minecraft.client.render.GameRenderer;
import net.minecraft.client.util.math.MatrixStack;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.gen.Invoker;

@Mixin(GameRenderer.class)
public interface IGameRenderer {
    @Invoker("getFov")
    public double invokeGetFov(Camera camera, float tickDelta, boolean changingFov);
    
    @Invoker("bobViewWhenHurt")
    public void invokeBobViewWhenHurt(MatrixStack matrices, float f);
    
    @Invoker("bobView")
    public void invokeBobView(MatrixStack matrices, float f);
}
